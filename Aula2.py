# Para fazer a declaração de váriaveis não é necessário colocar o tipo dela;
numeroInteiro   = 7
numeroDecimal   = 7.42566
palavra         = "Maria Eduarda"
verdadeiroFalso = True

# Mostra na tela os valores armazenados nas variáveis;
print(type(numeroInteiro))
